package JavaFundamentals.FunctionsJava;

public class VoidFunction {

  public static void printHelloWorld(){
    System.out.println("Hello World");
    System.out.println("Hello World");
    System.out.println("Hello World");

    //return type is void, there no return is needed

  }
  public static void main(String[] args) { // main function
    printHelloWorld(); // calling function in main function is important, cause java reads the main function 
  }
  
}
